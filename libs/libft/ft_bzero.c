#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			c;
	unsigned char	*d;

	d = (unsigned char *)s;
	c = 0;
	while (c < n)
	{
		d[c] = '\0';
		c++;
	}
}
